// Application logic
document.addEventListener('DOMContentLoaded', () => {
    // 1. Check if data is loaded
    if (typeof scholarshipsData === 'undefined') {
        console.error('Data not loaded!');
        return;
    }

    // State
    const container = document.getElementById('scholarshipsContainer');
    const paginationContainer = document.getElementById('paginationContainer');
    const searchInput = document.getElementById('searchInput');
    const countryFilter = document.getElementById('countryFilter');
    const fundingFilter = document.getElementById('fundingFilter');

    let currentPage = 1;
    const itemsPerPage = 10;
    let filteredData = [...scholarshipsData];

    function renderScholarships() {
        if (!container) return;
        container.innerHTML = '';

        const startIndex = (currentPage - 1) * itemsPerPage;
        const endIndex = startIndex + itemsPerPage;
        const paginatedData = filteredData.slice(startIndex, endIndex);

        if (paginatedData.length === 0 && currentPage === 1) {
            container.innerHTML = `
                <div class="no-results text-center py-20 bg-white rounded-2xl border flex-center flex-column gap-4" style="grid-column: 1 / -1; width: 100%;">
                    <i data-lucide="info" style="width: 48px; height: 48px; color: var(--text-faint);"></i>
                    <h3 class="text-xl font-bold">عذراً، لم نجد منحاً تطابق بحثك</h3>
                    <p class="text-muted">حاول تغيير كلمات البحث أو الفلاتر المختارة.</p>
                </div>
            `;
            if (window.lucide) lucide.createIcons();
            return;
        }

        paginatedData.forEach((item, index) => {
            const card = document.createElement('div');
            card.className = 'scholarship-card animate-zoom-in';
            card.style.animationDelay = `${index * 0.05}s`;

            card.innerHTML = `
                <div class="card-header" style="background: #f1f5f9;">
                    <img src="${item.image}" alt="${item.title}" loading="lazy" onerror="this.src='https://images.unsplash.com/photo-1523240795612-9a054b0db644?auto=format&fit=crop&q=80&w=800'; this.onerror=null;">
                    <div class="card-tag">${item.degree}</div>
                    <div class="country-flag">
                        <img src="https://flagcdn.com/w80/${item.countryCode}.png" alt="${item.country}">
                    </div>
                </div>
                <div class="card-body">
                    <h3 class="font-bold leading-tight line-clamp-2">${item.title}</h3>
                    <div class="card-info">
                        <div class="info-item">
                            <i data-lucide="map-pin"></i>
                            <span>${item.country}</span>
                        </div>
                        <div class="info-item">
                            <i data-lucide="dollar-sign"></i>
                            <span>تمويل ${item.funded}</span>
                        </div>
                    </div>
                    <button class="btn-primary w-full view-details" data-id="${item.id}" style="padding: 0.6rem; font-size: 0.8rem; margin-top: 0.5rem;">
                        التفاصيل
                    </button>
                </div>
            `;
            container.appendChild(card);
        });

        if (window.lucide) lucide.createIcons();

        document.querySelectorAll('.view-details').forEach(btn => {
            btn.addEventListener('click', () => {
                const id = btn.dataset.id;
                window.location.href = `details.html?id=${id}`;
            });
        });

        renderPagination();
    }

    function renderPagination() {
        if (!paginationContainer) return;
        paginationContainer.innerHTML = '';

        const totalPages = Math.ceil(filteredData.length / itemsPerPage);
        if (totalPages <= 1) return;

        for (let i = 1; i <= totalPages; i++) {
            const btn = document.createElement('button');
            btn.className = `btn-page ${i === currentPage ? 'active' : ''}`;
            btn.innerText = i;
            btn.addEventListener('click', () => {
                currentPage = i;
                renderScholarships();
                window.scrollTo({ top: container.offsetTop - 100, behavior: 'smooth' });
            });
            paginationContainer.appendChild(btn);
        }
    }

    function handleFilters() {
        const searchTerm = (searchInput ? searchInput.value : "").toLowerCase();
        const countryValue = countryFilter ? countryFilter.value : "all";
        const fundingValue = fundingFilter ? fundingFilter.value : "all";

        filteredData = scholarshipsData.filter(item => {
            const matchesSearch = item.title.toLowerCase().includes(searchTerm) ||
                (item.specializations && item.specializations.some(s => s.toLowerCase().includes(searchTerm))) ||
                item.country.includes(searchTerm);

            const matchesCountry = countryValue === 'all' || item.countryCode === countryValue;
            const matchesFunding = fundingValue === 'all' || item.funded === fundingValue;

            return matchesSearch && matchesCountry && matchesFunding;
        });

        currentPage = 1;
        renderScholarships();
    }

    if (searchInput) searchInput.addEventListener('input', handleFilters);
    if (countryFilter) countryFilter.addEventListener('change', handleFilters);
    if (fundingFilter) fundingFilter.addEventListener('change', handleFilters);

    window.addEventListener('scroll', () => {
        const header = document.querySelector('.glass-header');
        if (header) {
            if (window.scrollY > 50) {
                header.classList.add('header-scrolled');
            } else {
                header.classList.remove('header-scrolled');
            }
        }
    });

    renderScholarships();
});

